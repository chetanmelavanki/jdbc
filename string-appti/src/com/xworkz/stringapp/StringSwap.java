package com.xworkz.stringapp;

public class StringSwap {

	public static void main(String[] args) {
		String name1="ganesh";
		String name2="pitti";
		String nameTemp;
		System.out.println("before swap :"+name1+"  " +name2);
		nameTemp=name1;
		name1=name2;
		name2=nameTemp;
		System.out.println("before swap :"+name1+"  " +name2);

	}

}
